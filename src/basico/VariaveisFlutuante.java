package basico;

public class VariaveisFlutuante {
    
    public static void main (String [] args){
        double meuSalario;

        meuSalario = 123.45;
        System.out.println("meu salário é: " + meuSalario);

        double idade = 20;

        System.out.println("idade é: " + idade);

        double divisao = 3.14 / 3;
        System.out.println("Aqui é uma divisão com double: "+ divisao);

        int divisaoComInt = 5 / 3;
        System.out.println("Aqui é uma divisão com int: "+ divisaoComInt);

        double novaDivisaoComInt = 5 / 3;
        System.out.println("Aqui é uma divisão com int: "+ novaDivisaoComInt);

        
    }

}
