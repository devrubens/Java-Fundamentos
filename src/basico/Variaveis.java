package basico;

public class Variaveis {
    
    public static void main(String[] args){
        System.out.println("Olá novo teste para variaveis!");

        int idade;
        idade = 20;

        System.out.println("Idade é:" + idade);

        idade = 20 + 10;
        System.out.println("Idade é somada:" + idade);
        idade = 20 * 5;
        System.out.println("Idade é multiplicada:" + idade);
        idade = 20 / 40;
        System.out.println("Idade é dividida:" + idade);

    }

}
