package basico;

public class LacoDeRepeticao {
    public static void main(String[] args) {
        System.out.println("Laço de repetição While: ");
        
        int contador = 0;
        
        while (contador <= 10){
            System.out.println(contador);
            // contador = contador + 1
            //contador += 1;
            contador++;
            
        }
        System.out.println(contador);
    }
}
